int	ft_is_prime(int nb)
{
	int	i;
	int	a;

	i = 2;
	a = 0;
	if (nb <= 1)
		return (2);
	while (i <= nb)
	{
		if (nb % i == 0)
			a++;
		i++;
	}
	if (a == 1)
		return (1);
	return (0);
}
